#!/usr/bin/env node
/**
 * Pre-build script: generates TypeScript data files from .mdoc content files.
 * Run before `astro build` to embed Keystatic content into the app bundle.
 */
import { readFileSync, writeFileSync, readdirSync, mkdirSync } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';
import { load as yamlLoad } from 'js-yaml';

const __dirname = dirname(fileURLToPath(import.meta.url));
const PROJECT_ROOT = join(__dirname, '..');
const CONTENT_DIR = join(PROJECT_ROOT, 'src', 'content');
const DATA_DIR = join(PROJECT_ROOT, 'src', 'data');

function parseMdoc(raw) {
  const match = raw.match(/^---\n([\s\S]*?)\n---\n?([\s\S]*)$/);
  if (!match) throw new Error('Invalid .mdoc format');
  return { data: yamlLoad(match[1]), body: match[2].trim() };
}

function generateCollection(name) {
  const dir = join(CONTENT_DIR, name);
  if (!readdirSync(dir)) return [];

  const items = [];
  for (const fname of readdirSync(dir)) {
    if (!fname.endsWith('.mdoc')) continue;
    const raw = readFileSync(join(dir, fname), 'utf-8');
    const { data, body } = parseMdoc(raw);
    const slug = fname.replace('.mdoc', '');
    items.push({ slug, data, body });
  }

  const outPath = join(DATA_DIR, `${name}.ts`);
  mkdirSync(DATA_DIR, { recursive: true });

  const content = `// Auto-generated by scripts/gen-content-data.ts — DO NOT EDIT
// Source: src/content/${name}/*.mdoc

export interface ${capitalize(name)}Entry {
  slug: string;
  data: Record<string, unknown>;
  body: string;
}

export const ${name}: ${capitalize(name)}Entry[] = ${JSON.stringify(items, null, 2)};
`;

  writeFileSync(outPath, content, 'utf-8');
  console.log(`Generated ${outPath} (${items.length} entries)`);
  return items;
}

function capitalize(s) {
  return s.charAt(0).toUpperCase() + s.slice(1);
}

// Generate both collections
generateCollection('products');
generateCollection('solutions');
